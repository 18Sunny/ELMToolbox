classdef EEMELM < ELM
    properties
        maxNumberOfHiddenNeurons  = 1000
        pseudoInv
        maxError = 1e-3
        numberOfNeuronsByStep = 10
    end
    properties (Access = private)
        H
    end
    
    methods
        function self = EEMELM(varargin)
            self = self@ELM('numberOfHiddenNeurons',1,varargin{:});
        end
        
        function pi = pseudoinverse(~,h)
            if size(h,1)>=size(h,2)
                pi = pinv(h' * h) * h';
            else
                pi = h' * pinv(h * h');
            end
        end
        
        function self = train(self, X, Y)
            
            auxTime = toc;
            %Train with initial number of hidden neurons 
            tempH = X*self.inputWeight + repmat(self.biasOfHiddenNeurons,size(X,1),1);
            self.H = self.activationFunction(tempH);
            
            self.pseudoInv = self.pseudoinverse(self.H);
            self.outputWeight = self.pseudoInv * Y;
            
            E = norm(self.H*self.outputWeight - Y,'fro');
            
            while (self.numberOfHiddenNeurons < self.maxNumberOfHiddenNeurons) && (E > self.maxError)
                
                self.numberOfHiddenNeurons = self.numberOfHiddenNeurons + 1;
                
                Wnew = cell(1,self.numberOfNeuronsByStep);
                Bnew = cell(1,self.numberOfNeuronsByStep);
                deltaH = cell(1,self.numberOfNeuronsByStep);
                pseudoInvs = cell(1,self.numberOfNeuronsByStep);
                
                for j=1:self.numberOfNeuronsByStep
                
                    Wnew{j} = rand(self.seed, self.numberOfInputNeurons, 1)*2-1;
                    Bnew{j} = rand(self.seed, 1, 1);

                    tempH = X*Wnew{j} + repmat(Bnew{j},size(X,1),1);
                    deltaH{j} = self.activationFunction(tempH);
                    clear tempH;

                    Dk1 = (self.H*self.pseudoInv);
                    Dk = (deltaH{j}'*(eye(size(Dk1)) - Dk1))/(deltaH{j}'*(eye(size(Dk1)) - Dk1)*deltaH{j});
                    
                    Uk = self.pseudoInv*(eye(size(Dk,2)) - deltaH{j}*Dk);
                    pseudoInvs{j} = [Uk; Dk];
                    outputWeight = pseudoInvs{j}*Y;

                    E(j) = norm([self.H, deltaH{j}]*outputWeight - Y,'fro');
                    
                end
                [~,idx] = min(E);
                E = E(idx);
                self.pseudoInv = pseudoInvs{idx};
                self.outputWeight = self.pseudoInv*Y;
                self.inputWeight = [self.inputWeight, Wnew{idx}];
                self.H = [self.H, deltaH{j}];
                self.biasOfHiddenNeurons = [self.biasOfHiddenNeurons, Bnew{idx}];
%                 disp(E)
                self.trainTime = toc - auxTime;
            end
            
        end
        
    end
end
